function calculate() {
  var currentLevel = parseInt(document.getElementById('E14').value);
  var nextLevel = parseInt(document.getElementById('E15').value);
  var totalRequirementsA = 0;
  var totalRequirementsB = 0;
  var blueAsh = 0;

  for (var i = currentLevel + 1; i < nextLevel; i++) {
    var levelVarA = 'A' + i;
    var levelVarB = 'B' + i;

    if (window[levelVarA] !== undefined) {
      totalRequirementsA += window[levelVarA];
    }
    if (i % 50 === 0 && window[levelVarB] !== undefined) {
      totalRequirementsB += window[levelVarB];
    }
  }

  blueAsh = totalRequirementsA + totalRequirementsB;

  document.getElementById('M10').innerText = 'مجموع المتطلبات : '  ;
  document.getElementById('T999').innerText = totalRequirementsA;
  document.getElementById('T888').innerText = totalRequirementsB;
  document.getElementById('T10').innerText = 'رماد أزرق: ';
}

document.getElementById('B').addEventListener('click', calculate);
